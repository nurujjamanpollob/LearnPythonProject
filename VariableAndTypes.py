# Simple integer (No explicit object declaration)
myInteger = 7

# Print the number
print("The simple integer without explicitly defined type value is : %s" % myInteger)

# Define a floating type number (No explicit object declaration)
myFloatObj = 7.0

# Print the floating point number
#  to print in same line with String
print("The simple floating point number without explicitly defined type value is : %s" % myFloatObj)

# Define a floating point number with explicit type declaration
myFloat = float(7.0)

# Print the floating point number, using str(object) method to convert floating point number to a String
#  to print in same line with String
print("The floating point number with explicitly defined type value is %s" % myFloat)

# Define a String (No explicit object declaration)
myStrObj = "Hi, this is python String, without explicitly defined its type"

# Print that String
print(myStrObj)

# Define a String with explicitly type defined
myStr = str("Hi this is string with explicit type defined")

# Print that String
print(myStr)

# Define a Boolean (Value only True and False)
myBooleanObj = True

# Print
print("This is a python boolean, value is %s " % myBooleanObj)

# Define a Boolean (Explicitly defined)
myBoolean = bool(False)

# Print
print("Python boolean with explicitly type defined, value is %s" % myBoolean)

# Define a Byte Data type(Explicitly defined)
myByte = bytes(10)

# Print
print("Python byte data type example, value is %s" % myByte)
